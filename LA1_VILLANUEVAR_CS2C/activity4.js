// Comparison or Relational Operator

const firstFavNum = 21
const secondFavNum = 96
const greaterThan = firstFavNum > secondFavNum;
const lessThan = firstFavNum < secondFavNum;
const greatherThanOrEqual = firstFavNum >= secondFavNum;
const lessThanOrEqual = firstFavNum <= secondFavNum;
const strictEqual = firstFavNum === secondFavNum;
const looseEqual = firstFavNum == secondFavNum;
const strictNotEqual = firstFavNum !== secondFavNum;
const looseNotEqual = firstFavNum != secondFavNum;

console.log("greatherThan:" , greaterThan);
console.log("lessThan:", lessThan);
console.log("greatherThanOrEqual:", greatherThanOrEqual);
console.log("lessthanOrEqual:", lessThanOrEqual);
console.log("strictEqual:", strictEqual);
console.log("looseEqual:", looseEqual);
console.log("strictNotEqual:", strictNotEqual);
console.log("looseNotEqual:", looseNotEqual);